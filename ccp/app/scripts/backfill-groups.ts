  // "recovery period" here is an alarm evaluation parameter (how long before
  // ALARM auto-returns to OK), not disaster/backup recovery.
  'cloudwatch-alarm-promql-recovery-period': 'monitoring-alarms',
  // EC2 auto-recovery reboots/migrates on hardware failure — an availability
  // (auto-healing) feature, not data protection.
  'ec2-set-auto-recovery': 'availability-lifecycle',
  // ML-model "deployment" toggle, not a software/infra deployment-lifecycle
  // op; keep consistent with its user-profile sibling
