function classify(op: OpForClassify): Group {
  // Idempotency: a re-run against an already-backfilled catalog (e.g. this
  // script's own second pass, or a future re-run) must never reclassify an
  // op that's already labeled — keep whatever a human may have hand-adjusted.
  if (op.group) return op.group;
  const explicit = EXPLICIT[op.id];
  if (explicit) return explicit;
  if (op.macd === 'Add') return 'create';
  if (op.macd === 'Delete' || op.macd === 'Move' || op.forcesReplace) return 'danger-zone';

  const hay = `${op.id} ${op.title}`;
  if (TAGS_NAMING_RE.test(hay)) return 'tags-naming';
  if (PROTECTION_RE.test(hay)) return 'protection-backup';
  if (MONITORING_RE.test(hay)) return 'monitoring-alarms';
  if (AVAILABILITY_RE.test(hay)) return 'availability-lifecycle';
  if (SCALE_RE.test(hay)) return 'scale-performance';
  if (CONNECTIVITY_RE.test(hay)) return 'connectivity-access';

  const byResourceType = RESOURCE_TYPE_DEFAULT[op.target.resourceType];
  if (byResourceType) return byResourceType;
  throw new Error(
    `backfill-groups: no rule matched op "${op.id}" (resourceType ${op.target.resourceType}) — ` +
      `add a keyword rule or a RESOURCE_TYPE_DEFAULT entry before re-running.`,
  );
}

// ---------------------------------------------------------------------------
// Pinned — up to 5 common SAFE Change ops per resourceType. "Safe" = not in
// the forbidden-for-pinning set (Delete/Move/engineer_only/forcesReplace —
// Add is also excluded here since Add ops never appear in the per-resource
// picker this field feeds; they get their own "Add new" grid). Ranked
// lowest-risk-first so genuinely low-stakes ops fill the shortlist before
// anything HIGH/guardrailed is ever pinned; ties break on catalog order for
// determinism.
// ---------------------------------------------------------------------------

const RISK_RANK: Record<OpForClassify['riskFloor'], number> = { LOW: 0, MEDIUM: 1, HIGH: 2 };
const EXPOSURE_RANK: Record<OpForClassify['exposure'], number> = {
  l1_self_service: 0,
  l1_with_guardrails: 1,
  engineer_only: 2,
};
const MAX_PINNED_PER_RESOURCE_TYPE = 5;

function computePinned(allOps: OpForClassify[]): Map<string, number> {
  const byResourceType = new Map<string, OpForClassify[]>();
  for (const op of allOps) {
    const list = byResourceType.get(op.target.resourceType) ?? [];
    list.push(op);
    byResourceType.set(op.target.resourceType, list);
  }

  const pinned = new Map<string, number>();
  for (const ops of byResourceType.values()) {
    const eligible = ops.filter(
      (o) => o.macd === 'Change' && o.exposure !== 'engineer_only' && !o.forcesReplace,
    );
    const ranked = [...eligible].sort(
      (a, b) => RISK_RANK[a.riskFloor] - RISK_RANK[b.riskFloor] || EXPOSURE_RANK[a.exposure] - EXPOSURE_RANK[b.exposure],
    );
    ranked.slice(0, MAX_PINNED_PER_RESOURCE_TYPE).forEach((op, i) => pinned.set(op.id, i + 1));
  }
  return pinned;
}

// ---------------------------------------------------------------------------
// Main — surgical per-file text edit (see file header for why not JSON.stringify).
// ---------------------------------------------------------------------------

// Trailing comma is optional so a re-run recognizes lines this script (or a
// prior run of it) has already touched, vs. the pristine "last field" form.
const TF_CAP_LINE_RE = /^(\s*)"terraformCapability":\s*(".*")(,)?$/;

interface CatalogResult {
  files: string[];
  allOps: OpForClassify[];
  groupOf: Map<string, Group>;
  pinnedOf: Map<string, number>;
}

/** Classify + pin + surgically rewrite every manifest JSON file in `dir`, as
 * its own independent catalog scope (pinned ranks are computed among only
 * this directory's ops — see BOOTSTRAP_MANIFESTS comment above for why). */
function backfillCatalog(dir: string): CatalogResult {
  const files = readdirSync(dir)
    .filter((f) => f.endsWith('.json'))
    .sort();

  // Pass 1: parse every file to build the whole-catalog op list (classify()
  // is per-op, but computePinned() needs every op in this catalog scope).
  const parsedByFile = new Map<string, { operations: OpForClassify[] }>();
  const allOps: OpForClassify[] = [];
  for (const f of files) {
    const raw = readFileSync(join(dir, f), 'utf8');
    const manifest = JSON.parse(raw) as { operations: OpForClassify[] };
    parsedByFile.set(f, manifest);
    allOps.push(...manifest.operations);
  }

  const groupOf = new Map<string, Group>();
  for (const op of allOps) groupOf.set(op.id, classify(op));
  const pinnedOf = computePinned(allOps);

  // Pass 2: surgical text edit per file.
  for (const f of files) {
    const path = join(dir, f);
    const text = readFileSync(path, 'utf8');
    const lines = text.split('\n');
    const ops = parsedByFile.get(f)!.operations;

    let opIndex = 0;
    const out: string[] = [];
    for (const line of lines) {
      const m = TF_CAP_LINE_RE.exec(line);
      if (!m) {
        out.push(line);
        continue;
      }
      const op = ops[opIndex];
      opIndex++;
      if (!op) throw new Error(`${f}: more "terraformCapability" lines than parsed operations`);
      if (op.group) {
        // Idempotency: already backfilled in a prior run (this op's group is
        // already followed by its group/pinned lines) — leave untouched.
        out.push(line);
        continue;
      }
      const indent = m[1]!;
      const group = groupOf.get(op.id);
      if (!group) throw new Error(`${f}: op ${op.id} has no computed group`);
      const pinned = pinnedOf.get(op.id);

      out.push(`${indent}"terraformCapability": ${m[2]!},`);
      if (pinned === undefined) {
        out.push(`${indent}"group": "${group}"`);
      } else {
        out.push(`${indent}"group": "${group}",`);
        out.push(`${indent}"pinned": ${pinned}`);
      }
    }
    if (opIndex !== ops.length) {
      throw new Error(`${f}: found ${opIndex} "terraformCapability" lines but parsed ${ops.length} operations`);
    }
    writeFileSync(path, out.join('\n'));
  }

  return { files, allOps, groupOf, pinnedOf };
}

function reportPinned(label: string, result: CatalogResult): void {
  const resourceTypesWithPinned = new Set(
    [...result.pinnedOf.keys()].map((id) => result.allOps.find((o) => o.id === id)!.target.resourceType),
  );
  console.log(
    `${label}: ${result.pinnedOf.size} ops pinned across ${resourceTypesWithPinned.size} resourceTypes (max ${MAX_PINNED_PER_RESOURCE_TYPE} each)`,
  );
}

function main(): void {
  const mainCatalog = backfillCatalog(MANIFESTS);
  if (mainCatalog.allOps.length !== 680) {
    console.warn(
      `WARNING: expected 680 ops in the main catalog, found ${mainCatalog.allOps.length} — histogram/measured comparison will be off.`,
    );
  }

  const bootstrap = backfillCatalog(BOOTSTRAP_MANIFESTS);

  // --- report (main catalog — the 0008 §3 measured comparison is scoped to it) ---
  const MEASURED: Record<Group, number> = {
    'scale-performance': 97,
    'availability-lifecycle': 40,
    'connectivity-access': 138,
    'protection-backup': 104,
    'monitoring-alarms': 68,
    'tags-naming': 111,
    'danger-zone': 65,
    create: 82,
  };
  const order: Group[] = [
    'create',
    'danger-zone',
    'scale-performance',
    'availability-lifecycle',
    'connectivity-access',
    'protection-backup',
    'monitoring-alarms',
    'tags-naming',
  ];
  const histogram = new Map<Group, number>();
  for (const g of mainCatalog.groupOf.values()) histogram.set(g, (histogram.get(g) ?? 0) + 1);

  console.log('\n=== group histogram vs measured (0008 §3) — main catalog ===');
  let total = 0;
  for (const g of order) {
    const actual = histogram.get(g) ?? 0;
    total += actual;
    const delta = actual - MEASURED[g];
    const sign = delta >= 0 ? '+' : '';
    console.log(`${g.padEnd(24)} actual=${String(actual).padStart(4)}  measured~${String(MEASURED[g]).padStart(4)}  delta=${sign}${delta}`);
  }
  console.log(`TOTAL ${total} (680 ops across ${mainCatalog.files.length} manifest files)`);

  console.log(`\n=== pinned ===`);
  reportPinned('main catalog', mainCatalog);
  reportPinned('bootstrap project catalog', bootstrap);
}

main();
