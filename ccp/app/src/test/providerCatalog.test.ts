describe('hybrid linkage — the estate’s hand-authored create forms lead', () => {
  const byType = manifestCreateOpsByType(manifests);

  it('finds the bundled create baselines by the type they stand up', () => {
    // The estate catalog provisions EC2 instances and S3 buckets by hand.
    expect(byType.get('aws_instance')?.some((op) => op.id === 'ec2-provision-instance')).toBe(true);
    expect(byType.get('aws_s3_bucket')?.some((op) => op.id === 's3-create-bucket')).toBe(true);
  });

  it('createdTypeOf reads the synthesized block when one is named (aws or azurerm)', () => {
    expect(
      createdTypeOf({
        codemodOp: 'append_block',
        target: { resourceType: 'aws_ebs_volume', block: 'aws_ebs_snapshot.<name>' },
      } as never),
    ).toBe('aws_ebs_snapshot');
    expect(
      createdTypeOf({
        codemodOp: 'append_block',
        target: { resourceType: 'azurerm_virtual_network', block: 'azurerm_subnet.<name>' },
      } as never),
    ).toBe('azurerm_subnet');
  });
});

describe('groupProviderServices — category grouping preserves generated order', () => {
  const index: CatalogIndex = {
    provider: 'hashicorp/aws',
    providerVersion: '6.53.0',
    services: [
      { slug: 'ec2', name: 'EC2', category: 'Compute', types: [] },
      { slug: 'lambda', name: 'Lambda', category: 'Compute', types: [] },
      { slug: 's3', name: 'S3', category: 'Storage', types: [] },
    ],
  };
  it('one group per category run, services kept in order', () => {
    const groups = groupProviderServices(index);
    expect(groups.map((g) => g.category)).toEqual(['Compute', 'Storage']);
    expect(groups[0]?.services.map((s) => s.slug)).toEqual(['ec2', 'lambda']);
  });
});

describe('paramsFor — ordering', () => {
  it('top-level attributes come before nested-block fields', () => {
    const names = paramsFor(RES).map((p) => p.name);
    expect(names.indexOf('queue_name')).toBeLessThan(names.indexOf('encryption.kms_key_id'));
    expect(names.indexOf('encryption.kms_key_id')).toBeLessThan(names.indexOf('rule'));
  });
});
